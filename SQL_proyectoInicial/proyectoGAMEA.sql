-- Creacion de la tabla de sessiones
CREATE TABLE IF NOT EXISTS tbl_sessions (
id serial,
session_id varchar(40) NOT NULL DEFAULT '0',
ip_address varchar(16) NOT NULL DEFAULT '0',
user_agent varchar(120) DEFAULT NULL,
last_activity int NOT NULL DEFAULT '0',
user_data text NOT NULL,
PRIMARY KEY (session_id)
) 

--Creacion de la tabla de usuarios

CREATE TABLE tbl_usuarios (

id serial,
id_usua int NOT NULL,
nombre_usua varchar(125) NOT NULL,
apellidopat_usua varchar(125) NOT NULL,
apellidomat_usua varchar(255) NOT NULL,
fecha_cre_usua varchar(125) NOT NULL,
ci_usua varchar(125) NOT NULL,
cargo_usua varchar(125) NOT NULL,
estado varchar(3) NOT NULL,
PRIMARY KEY (id_usua)
) 

--creacion de tabla de catalogo de distritos

CREATE TABLE cat_distritos (

id serial,
id_dist int NOT NULL,
nombre_dist varchar(200) NOT NULL,
observacion_dist varchar(300) NOT NULL,
fecha_reg_dist varchar(125) NOT NULL,
fecha_mod_dist varchar(125) NOT NULL,
estado_dist varchar(3) NOT NULL,
PRIMARY KEY (id_dist)
) 

--Catalogo de urbaniuzaciones

CREATE TABLE cat_urbanizaciones (

id serial,
id_urb int NOT NULL,
id_dist int NOT NULL,
nombre_urb varchar(200) NOT NULL,
observacion_urb varchar(300) NOT NULL,
fecha_reg_urb varchar(125) NOT NULL,
fecha_mod_urb varchar(125) NOT NULL,
estado_urb varchar(3) NOT NULL,
PRIMARY KEY (id)
) 

--Presupuestos vecinales

create table tbl_presupuestos(
id serial,
id_pres int NOT NULL,
monto_pres double precision Not null,
fecha_reg_pres varchar(125) NOT NULL,
fecha_mod_pres varchar(125) NOT NULL,
id_urb int NOT NULL,
id_dist int NOT NULL,
PRIMARY KEY (id_pres)
)

-- Catalogo de documentacion fisica

CREATE TABLE cat_documentacion (

id serial,
id_doc int NOT NULL,
tipo_doc varchar(200) NOT NULL,
fecha_reg_doc varchar(125) NOT NULL,
fecha_mod_doc varchar(125) NOT NULL,
estado_doc varchar(3) NOT NULL,
PRIMARY KEY (id_doc)
) 

-- Catalogo de planificacion de proyectos


CREATE TABLE cat_clasificacion (

id serial,
id_clas int NOT NULL,
tipo_clas varchar(200) NOT NULL,
fecha_reg_clas varchar(125) NOT NULL,
fecha_mod_clas varchar(125) NOT NULL,
estado_clas varchar(3) NOT NULL,
PRIMARY KEY (id_clas)
) 

-- Catalog de categortia de proyecto


CREATE TABLE cat_categoria (

id serial,
id_cat int NOT NULL,
nombre_cat varchar(200) NOT NULL,
fecha_reg_cat varchar(125) NOT NULL,
fecha_mod_cat varchar(125) NOT NULL,
estado_cat varchar(3) NOT NULL,
PRIMARY KEY (id_cat)
) 


-- cataloog de sub categoria de proyecto


CREATE TABLE cat_sub_categoria (

id serial,
id_cat int not null,
id_sub_cat int NOT NULL,
nombre_sub_cat varchar(200) NOT NULL,
fecha_reg_sub_cat varchar(125) NOT NULL,
fecha_mod_sub_cat varchar(125) NOT NULL,
estado_sub_cat varchar(3) NOT NULL,
PRIMARY KEY (id)
) 

